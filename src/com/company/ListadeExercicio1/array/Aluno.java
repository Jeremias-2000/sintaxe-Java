package com.company.ListadeExercicio1.array;

public class Aluno {

    private String nome;
    private double nota;

    public void setNome(String nome){
        this.nome = nome;
    }

    public String getNome(){
        return this.nome;
    }

    public void setNota(double nota){
        this.nota = nota;
    }

    public double getNota(){
        return this.nota;
    }
}
